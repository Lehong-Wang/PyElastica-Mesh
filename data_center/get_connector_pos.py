import numpy as np

def node_frame(d1_sign: int, dtype=np.float32) -> np.ndarray:
    """
    Return a single 3x3 director frame (as provided by you).
    """
    if d1_sign == -1:
        return np.array(
            [[0.0, 0.0,  1.0],
             [0.0, -1.0, 0.0],
             [1.0, 0.0,  0.0]],
            dtype=dtype,
        )
    else:
        return np.array(
            [[0.0, 0.0, -1.0],
             [0.0,  1.0, 0.0],
             [1.0, 0.0,  0.0]],
            dtype=dtype,
        )

def make_directors_for_pos(pos: np.ndarray, d1_sign: int, dtype=np.float32) -> np.ndarray:
    """
    pos: (N,3) -> directors: (N,3,3), one-to-one with nodes
    """
    pos = np.asarray(pos, dtype=dtype)
    if pos.ndim != 2 or pos.shape[1] != 3:
        raise ValueError(f"pos must be (N,3), got {pos.shape}")
    N = pos.shape[0]
    frame = node_frame(d1_sign, dtype=dtype)          # (3,3)
    directors = np.repeat(frame[None, :, :], N, 0)    # (N,3,3)
    return directors

def compose_pos_director(pos_a: np.ndarray, sign_a: int,
                         pos_b: np.ndarray, sign_b: int,
                         dtype=np.float32, join_tolerance=1e-6):
    """
    Compose ONE (pos, director) from two pos segments with different directors.
    Ensures pos (N,3) and director (N,3,3) correspond 1-to-1.

    If pos_a[-1] == pos_b[0] (within tolerance), drop pos_b[0] to avoid duplicate node.
    """
    pos_a = np.asarray(pos_a, dtype=dtype)
    pos_b = np.asarray(pos_b, dtype=dtype)
    if pos_a.ndim != 2 or pos_a.shape[1] != 3:
        raise ValueError(f"pos_a must be (N,3), got {pos_a.shape}")
    if pos_b.ndim != 2 or pos_b.shape[1] != 3:
        raise ValueError(f"pos_b must be (M,3), got {pos_b.shape}")
    if pos_a.shape[0] < 1 or pos_b.shape[0] < 1:
        raise ValueError("Both segments must have at least 1 point.")

    # dedup junction if needed
    if np.linalg.norm(pos_a[-1] - pos_b[0]) <= join_tolerance:
        pos_b = pos_b[1:]

    pos = np.vstack([pos_a, pos_b]) if pos_b.shape[0] else pos_a

    dir_a = make_directors_for_pos(pos_a, sign_a, dtype=dtype)
    dir_b = make_directors_for_pos(pos_b, sign_b, dtype=dtype) if pos_b.shape[0] else np.zeros((0,3,3), dtype=dtype)
    directors = np.concatenate([dir_a, dir_b], axis=0)

    if pos.shape[0] != directors.shape[0]:
        raise RuntimeError(f"pos/director mismatch: {pos.shape[0]} vs {directors.shape[0]}")

    return pos, directors

def save_npz(out_path: str, pairs, dtype=np.float32, join_tolerance=1e-6):
    """
    Save up to 4 composed pairs into an npz with keys:
      "1_pos","1_director","2_pos","2_director",...

    Each item in `pairs` can be either:
      A) single segment:
         {"id": 1, "pos": pos, "sign": +1 or -1}
      B) two-segment compose (your requested case):
         {"id": 1, "pos_a": pos1a, "sign_a": +1/-1, "pos_b": pos1b, "sign_b": +1/-1}

    All saved directors are node-wise (N,3,3) and correspond 1-to-1 with pos (N,3).
    """
    if not isinstance(pairs, (list, tuple)) or len(pairs) == 0:
        raise ValueError("pairs must be a non-empty list/tuple.")
    if len(pairs) > 4:
        raise ValueError("Up to 4 pairs supported.")

    payload = {}
    for k, item in enumerate(pairs, start=1):
        idx = int(item.get("id", k))

        if "pos_a" in item:
            pos, director = compose_pos_director(
                item["pos_a"], int(item["sign_a"]),
                item["pos_b"], int(item["sign_b"]),
                dtype=dtype,
                join_tolerance=join_tolerance,
            )
        else:
            pos = np.asarray(item["pos"], dtype=dtype)
            director = make_directors_for_pos(pos, int(item["sign"]), dtype=dtype)

        payload[f"{idx}_pos"] = pos
        payload[f"{idx}_director"] = director

    np.savez_compressed(out_path, **payload)
    print(f"Saved: {out_path}")
    for i in sorted({int(k.split("_")[0]) for k in payload.keys()}):
        p = payload[f"{i}_pos"]
        d = payload[f"{i}_director"]
        print(f"  {i}_pos: {p.shape}   {i}_director: {d.shape}")

